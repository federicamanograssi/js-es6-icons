// 01.2 FUNCTION PER STAMPARE ICONE
function print(array,container){
	array.forEach((icon) =>{
		const {name,prefix,family} = icon;
	
		const iconHTML =`
		<div>
			<i class="${family} ${prefix}${name}"></i>
			<div>${name}</div>
		</div>
		`;
	
		container.append(iconHTML);
	})

}
